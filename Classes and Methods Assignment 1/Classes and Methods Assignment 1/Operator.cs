using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Classes_and_Methods_Assignment_1
{
    class Operator
    {
        
        public int Add(int data, int value)
        {
            return data + value;
    
        }

        public int Subtract(int data, int value)
        {
            return data - value;
        }

        public int Divide(int data, int value)
        {
            return data / value;

        }

        public double Decimal( double data, double value)
        {
            return  data * value; 
        }


    }
}
